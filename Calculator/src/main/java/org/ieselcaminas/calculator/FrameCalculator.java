    private void addNumberToDisplay(String numberStr) {
        if (cleanDisplay) {
            accumulator = Double.parseDouble(textFieldDiplay.getText());
            textFieldDiplay.setText(numberStr);
            cleanDisplay = false;
        } else {
            String s = textFieldDiplay.getText();
            s += numberStr;
            textFieldDiplay.setText(s);
        }
    }
