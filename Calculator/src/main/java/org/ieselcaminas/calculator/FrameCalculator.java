    private void myInits() {
        operation = Operators.NONE;
        accumulator = 0;
        cleanDisplay = false;
    }
    
    private void showResultOnDisplay(double value) {
        String valStr = "" + value;
        valStr = valStr.replaceAll("\\.0", "");
        textFieldDiplay.setText(valStr);
        cleanDisplay = true;
    }
    
    private double getDisplayValue() {
        String valStr = textFieldDiplay.getText();
        return Double.parseDouble(valStr);
    }
    
    private void calculate() {
        switch (operation) {
            case ADD:
                accumulator += getDisplayValue();
                break;
            case SUB:
                accumulator -= getDisplayValue();
                break;
            case MULT:
                accumulator *= getDisplayValue();
                break;
            case DIV:
                accumulator /= getDisplayValue();
                break;
            default:
                accumulator = getDisplayValue();
                break;
        }
        showResultOnDisplay(accumulator);
    }
